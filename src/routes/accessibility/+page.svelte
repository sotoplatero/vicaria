			<h2>Our Commitment</h2>
			<p>
				Vicaria Health is committed to ensuring accessibility for all people, including those with disabilities. We strive to provide equal access to our health coaching services, skin treatments, and digital content for everyone in our community.
			</p>
			<p>
				We are committed to complying with the Accessibility for Ontarians with Disabilities Act (AODA) and its associated standards, including the Integrated Accessibility Standards Regulation (IASR).
			</p>

			<h2>Physical Accessibility</h2>

			<h3>Our Location</h3>
			<p>
				Our office is located at Unit 2B 144 James St S, Hamilton, ON L8P 3A2. We have taken the following measures to ensure physical accessibility:
			</p>
			<ul>
				<li><strong>Accessible Entrance:</strong> Our building has an accessible entrance with level access</li>
				<li><strong>Elevator Access:</strong> Our suite is accessible via elevator</li>
				<li><strong>Wide Doorways:</strong> Doorways are wide enough to accommodate wheelchairs and mobility devices</li>
				<li><strong>Accessible Washrooms:</strong> Accessible washroom facilities are available in the building</li>
				<li><strong>Service Animals:</strong> Service animals are welcome in all areas of our practice</li>
				<li><strong>Support Persons:</strong> Support persons are welcome to accompany clients during appointments</li>
			</ul>

			<h3>Parking</h3>
			<p>
				Accessible parking is available nearby. Please contact us in advance if you have specific parking needs, and we will provide detailed directions.
			</p>

			<h2>Service Accessibility</h2>

			<h3>Communication</h3>
			<p>
				We are committed to communicating with people with disabilities in ways that take into account their disability:
			</p>
			<ul>
				<li><strong>Multiple Contact Methods:</strong> We offer phone, email, and online booking options</li>
				<li><strong>Alternative Formats:</strong> We can provide documents and materials in alternative formats upon request (large print, digital text, audio)</li>
				<li><strong>Clear Communication:</strong> Our staff are trained to communicate clearly and patiently</li>
				<li><strong>Virtual Appointments:</strong> We offer virtual consultation options for those unable to visit in person</li>
				<li><strong>Assistive Devices:</strong> You are welcome to use your own assistive devices during appointments</li>
			</ul>

			<h3>Service Accommodations</h3>
			<p>
				We strive to accommodate individual needs:
			</p>
			<ul>
				<li>Extended appointment times when needed</li>
				<li>Flexible scheduling to accommodate medical appointments or treatments</li>
				<li>Quiet, low-stimulation environment available upon request</li>
				<li>Written summaries of appointment discussions</li>
				<li>Home visit consultations for those unable to travel (where feasible)</li>
			</ul>

			<h2>Website Accessibility</h2>
			<p>
				We are committed to making our website accessible and user-friendly for all visitors. Our website aims to comply with the Web Content Accessibility Guidelines (WCAG) 2.1 Level AA standards.
			</p>

			<h3>Accessibility Features</h3>
			<ul>
				<li><strong>Keyboard Navigation:</strong> All functionality is accessible via keyboard</li>
				<li><strong>Screen Reader Compatibility:</strong> Our site is designed to work with common screen readers</li>
				<li><strong>Alternative Text:</strong> Images include descriptive alt text</li>
				<li><strong>Semantic HTML:</strong> Proper heading structure and semantic markup for easier navigation</li>
				<li><strong>Color Contrast:</strong> Sufficient color contrast for text readability</li>
				<li><strong>Resizable Text:</strong> Text can be resized without loss of functionality</li>
				<li><strong>Clear Navigation:</strong> Consistent and predictable navigation structure</li>
				<li><strong>Form Labels:</strong> All form fields have clear, descriptive labels</li>
				<li><strong>Focus Indicators:</strong> Visible focus indicators for keyboard navigation</li>
			</ul>

			<h3>Browser Compatibility</h3>
			<p>
				Our website is designed to work with modern web browsers and assistive technologies, including:
			</p>
			<ul>
				<li>Latest versions of Chrome, Firefox, Safari, and Edge</li>
				<li>JAWS, NVDA, and VoiceOver screen readers</li>
				<li>Browser zoom and text sizing features</li>
			</ul>

			<h2>Staff Training</h2>
			<p>
				All Vicaria Health staff members receive training on:
			</p>
			<ul>
				<li>The Accessibility for Ontarians with Disabilities Act (AODA)</li>
				<li>The Ontario Human Rights Code as it applies to persons with disabilities</li>
				<li>How to interact and communicate with people with various types of disabilities</li>
				<li>How to interact with people who use assistive devices or require service animals or support persons</li>
				<li>How to use equipment or devices available at our location to help people with disabilities</li>
				<li>What to do if a person with a disability is having difficulty accessing our services</li>
			</ul>

