			<h2>Accessible Documents</h2>
			<p>
				Upon request, we will provide or arrange for the provision of accessible formats and communication supports for persons with disabilities:
			</p>
			<ul>
				<li>Large print documents</li>
				<li>Digital text files compatible with screen readers</li>
				<li>Audio recordings</li>
				<li>Simplified language versions</li>
				<li>Visual aids and diagrams</li>
			</ul>
			<p>
				Please contact us as early as possible to allow us time to prepare materials in your preferred format.
			</p>

			<h2>Emergency Procedures</h2>
			<p>
				We have emergency procedures in place to ensure the safety of all clients, including those with disabilities. If you require specific assistance during an emergency, please inform us when booking your appointment so we can make appropriate arrangements.
			</p>

			<h2>Ongoing Improvement</h2>
			<p>
				Accessibility is an ongoing effort. We are committed to continually improving accessibility through:
			</p>
			<ul>
				<li>Regular accessibility audits of our facilities and website</li>
				<li>Ongoing staff training and education</li>
				<li>Consulting with accessibility experts and the disability community</li>
				<li>Implementing new accessibility features and technologies as they become available</li>
				<li>Regular review and update of our accessibility policies</li>
			</ul>

			<h2>Known Limitations</h2>
			<p>
				While we strive for full accessibility, we acknowledge some current limitations:
			</p>
			<ul>
				<li>Some third-party content or links on our website may not be fully accessible</li>
				<li>Certain skin treatment procedures may have physical accessibility limitations</li>
				<li>Some older documents may not yet be available in all alternative formats</li>
			</ul>
			<p>
				We are actively working to address these limitations. If you encounter any barriers, please contact us so we can find alternative solutions.
			</p>

